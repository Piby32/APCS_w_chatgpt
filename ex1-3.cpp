#include <stdio.h>

int main(){
    int a = 10, b = 3;
    printf("加法: %d + %d = %d\n", a, b, a + b);
    printf("檢法: %d - %d = %d\n", a, b, a - b);
    printf("乘法: %d * %d = %d\n", a, b, a * b);
    printf("除法: %d / %d = %d\n", a, b, a / b);
    printf("曲除法: %d %% %d = %d\n", a, b, a % b);
    return 0;
}
